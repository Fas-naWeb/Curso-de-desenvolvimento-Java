package _03_classesEMetodos;

public class _02_ConceitoDeMetodos {
    /**
       Metodos são codigos que executa alguma ação,e o verbo.Eles podem ou não receber parametros e podem ou não 
       retornar algum valor.Você pode ter metodos com o mesmo nome,mais não com a mesma assinatura.
     */
     // Metodo recebe dois parametros e retorna a soma.
     int soma(int a,int b){
       return a + b;
     }
}
